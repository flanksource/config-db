// LogOutput renders log lines with colored level prefixes.
// +kubebuilder:object:generate=false
type LogOutput struct {
	lines []string
}

func (l LogOutput) Pretty() api.Text {
	if len(l.lines) == 0 {
		return clicky.Text("")
	}
	t := colorLogLine(l.lines[0])
	for _, line := range l.lines[1:] {
		t = t.NewLine().Append(colorLogLine(line))
	}
	return clicky.Text("").Append(clicky.Collapsed(fmt.Sprintf("Logs (%d lines)", len(l.lines)), t))
}

func colorLogLine(line string) api.Text {
	loc := logLevelRegex.FindStringIndex(line)
	if loc == nil {
		return clicky.Text(line)
	}
	tag := strings.TrimSpace(line[loc[0]:loc[1]])
	base := tag
	if i := strings.IndexByte(tag, '-'); i >= 0 {
		base = tag[:i]
	}
	return clicky.Text(line[:loc[0]+1]).
		Append(tag, logLevelStyles[base]).
		Append(line[loc[1]-1:])
}

func BuildLogOutput(rawLogs string) LogOutput {
	cleaned := ansiEscapeRegex.ReplaceAllString(rawLogs, "")
	trimmed := strings.TrimRight(cleaned, "\n")
	if trimmed == "" {
		return LogOutput{}
	}
	return LogOutput{lines: strings.Split(trimmed, "\n")}
}

