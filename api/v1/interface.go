func MergeScrapeResults(results ...ScrapeResults) FullScrapeResults {
	full := FullScrapeResults{}
	for _, res := range results {
		for _, r := range res {
			if r.Error != nil {
				continue
			}

			if r.AnalysisResult != nil {
				full.Analysis = append(full.Analysis, r.AnalysisResult.ToConfigAnalysis())
			}

			full.Changes = append(full.Changes, r.Changes...)

			for _, rel := range r.RelationshipResults {
				full.Relationships = append(full.Relationships, models.ConfigRelationship{
					ConfigID:  rel.ConfigID,
					RelatedID: rel.RelatedConfigID,
					Relation:  rel.Relationship,
				})
			}

			if !r.IsMetadataOnly() {
				full.Configs = append(full.Configs, r)
			}

			full.ExternalRoles = append(full.ExternalRoles, r.ExternalRoles...)
			full.ExternalUsers = append(full.ExternalUsers, r.ExternalUsers...)
			full.ExternalGroups = append(full.ExternalGroups, r.ExternalGroups...)
			full.ExternalUserGroups = append(full.ExternalUserGroups, r.ExternalUserGroups...)
			full.ConfigAccess = append(full.ConfigAccess, r.ConfigAccess...)
			full.ConfigAccessLogs = append(full.ConfigAccessLogs, r.ConfigAccessLogs...)
		}
	}

	// Collapse duplicate user/group/role rows that came in via different
	// scrape paths (graph-permissions, ACL paths, per-project loops). Each
	// path emits its own ScrapeResult; the per-project entityCtx in
	// pipelines.go only deduplicates within one project, so cross-project
	// and cross-source duplicates land here. Two rows are the same logical
	// entity iff they share an id (when non-nil) or any alias.
	full.ExternalUsers = clusterByIDOrAlias(
		full.ExternalUsers,
		func(u models.ExternalUser) uuid.UUID { return u.ID },
		func(u models.ExternalUser) []string { return u.Aliases },
		func(u *models.ExternalUser, a []string) { u.Aliases = a },
	)
	full.ExternalGroups = clusterByIDOrAlias(
		full.ExternalGroups,
		func(g models.ExternalGroup) uuid.UUID { return g.ID },
		func(g models.ExternalGroup) []string { return g.Aliases },
		func(g *models.ExternalGroup, a []string) { g.Aliases = a },
	)
	full.ExternalRoles = clusterByIDOrAlias(
		full.ExternalRoles,
		func(r models.ExternalRole) uuid.UUID { return r.ID },
		func(r models.ExternalRole) []string { return r.Aliases },
		func(r *models.ExternalRole, a []string) { r.Aliases = a },
	)
	return full
}

// clusterByIDOrAlias collapses a slice of entities by id-equality (when
// non-nil) and alias-overlap (always). The first row in each cluster is the
// survivor and inherits the union of all member aliases; subsequent rows are
// dropped. This is the upstream chokepoint that ensures every downstream
// consumer (UI, save, snapshot) sees one row per logical entity.
func clusterByIDOrAlias[T any](
	items []T,
	getID func(T) uuid.UUID,
	getAliases func(T) []string,
	setAliases func(*T, []string),
) []T {
	if len(items) <= 1 {
		return items
	}

	parent := make([]int, len(items))
	for i := range parent {
		parent[i] = i
	}
	var find func(int) int
	find = func(x int) int {
		if parent[x] != x {
			parent[x] = find(parent[x])
		}
		return parent[x]
	}
	union := func(a, b int) {
		ra, rb := find(a), find(b)
		if ra != rb {
			parent[ra] = rb
		}
	}

	idToIdx := make(map[uuid.UUID]int)
	aliasToIdx := make(map[string]int)
	for i, item := range items {
		if id := getID(item); id != uuid.Nil {
			if existingIdx, ok := idToIdx[id]; ok {
				union(existingIdx, i)
			} else {
				idToIdx[id] = i
			}
		}
		for _, alias := range getAliases(item) {
			if alias == "" {
				continue
			}
			if existingIdx, ok := aliasToIdx[alias]; ok {
				union(existingIdx, i)
			} else {
				aliasToIdx[alias] = i
			}
		}
	}

	// First non-nil-id wins per cluster; otherwise the first encountered.
	survivor := make(map[int]int)
	for i := range items {
		root := find(i)
		cur, ok := survivor[root]
		if !ok {
			survivor[root] = i
			continue
		}
		if getID(items[cur]) == uuid.Nil && getID(items[i]) != uuid.Nil {
			survivor[root] = i
		}
	}

	rootToOutIdx := make(map[int]int)
	out := make([]T, 0, len(items))
	for i := range items {
		root := find(i)
		survivorIdx := survivor[root]
		outIdx, ok := rootToOutIdx[root]
		if !ok {
			outIdx = len(out)
			rootToOutIdx[root] = outIdx
			out = append(out, items[survivorIdx])
		}
		if i == survivorIdx {
			continue
		}
		seen := make(map[string]struct{})
		for _, a := range getAliases(out[outIdx]) {
			seen[a] = struct{}{}
		}
		merged := getAliases(out[outIdx])
		for _, a := range getAliases(items[i]) {
			if a == "" {
				continue
			}
			if _, ok := seen[a]; ok {
				continue
			}
			seen[a] = struct{}{}
			merged = append(merged, a)
		}
		setAliases(&out[outIdx], merged)
	}
	return out
}

func Ellipses(str string, length int) string {
	if len(str) > length {
		if len(str) < 3 || length < 3 {
			return str
		}
		return str[0:length/2] + "..." + str[len(str)-length/2-3:]
	}
	return str
}

func (r ScrapeResult) String() string {
	s := fmt.Sprintf("%s/%s (%s)", r.ConfigClass, Ellipses(r.Name, 40), Ellipses(r.ID, 40))

	if r.Health != models.HealthUnknown {
		s += fmt.Sprintf(" %s", r.Health)
	}

	if r.Status != "" {
		s += fmt.Sprintf(" %s", Ellipses(r.Status, 20))
	}

	if len(r.Changes) > 0 {
		s += fmt.Sprintf(" changes=%d", len(r.Changes))
	}
	if len(r.RelationshipResults) > 0 {
		s += fmt.Sprintf(" relationships=%d", len(r.RelationshipResults))
	}
	if r.AnalysisResult != nil {
		s += " analysis=1"
	}
	return s
}

// ConfigMap returns the underlying config as a map
func (r ScrapeResult) ConfigString() string {

	// If config is of type string, try unmarshal first
	if v, ok := r.Config.(string); ok {
		return v
	}

	// Marshal and unmarshal into json for other types
	b, err := json.Marshal(r.Config)
	if err != nil {
		logger.Errorf("failed to marshal config: %v", err)
		return ""
	}

	return string(b)
}

// ConfigMap returns the underlying config as a map
func (r ScrapeResult) ConfigMap() map[string]any {
	output := make(map[string]any)

	// If config is of type string, try unmarshal first
	if _, ok := r.Config.(string); ok {
		if err := json.Unmarshal([]byte(r.Config.(string)), &output); err != nil {
			logger.Errorf("failed to unmarshal config: %v", err)
		}
		return output
	}

	// Marshal and unmarshal into json for other types
	b, err := json.Marshal(r.Config)
	if err != nil {
		logger.Errorf("failed to marshal config: %v", err)
		return output
	}

	if err := json.Unmarshal(b, &output); err != nil {
		logger.Errorf("failed to unmarshal config: %v", err)
		return output
	}

	return output
}

